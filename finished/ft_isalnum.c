#include "libft.h"

int	ft_isalnum(int c)
{
	int	res;

	res = 0;
	if (c >= '0' && c <= '9')
		res = 1;
	if (c >= 'A' && c <= 'Z')
		res = 1;
	if (c >= 'a' && c <= 'z')
		res = 1;
	return (res);
}
