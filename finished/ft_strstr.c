#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	int	i;
	int	j;
	int	pos;

	i = 0;
	if (needle[0] == '\0')
		return ((char *)haystack);
	while (haystack[i])
	{
		j = 0;
		pos = i;
		while (haystack[pos] == needle[j])
		{
			pos++;
			j++;
		}
		if (needle[j] == '\0')
			return (&((char *)haystack)[i]);
		i++;
	}
	return (0);
}
